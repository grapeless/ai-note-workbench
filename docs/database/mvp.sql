CREATE EXTENSION IF NOT EXISTS vector;

CREATE TABLE IF NOT EXISTS knowledge_collection (
                                          id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                          name TEXT NOT NULL,
                                          description TEXT,
                                          create_time TIMESTAMP NOT NULL DEFAULT now(),
                                          update_time TIMESTAMP NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS document (
                                         id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                         collection_id BIGINT NOT NULL REFERENCES knowledge_collection(id) ON DELETE CASCADE,
                                         title TEXT NOT NULL,
                                         source_path TEXT NOT NULL,
                                         content_type TEXT NOT NULL,
                                         status TEXT NOT NULL DEFAULT 'UPLOADED',
                                         error_message TEXT,
                                         create_time TIMESTAMP NOT NULL DEFAULT now(),
                                         update_time TIMESTAMP NOT NULL DEFAULT now(),

                                         CONSTRAINT documents_status_check
                                             CHECK (status IN ('UPLOADED', 'PARSING', 'PARSED', 'EMBEDDED', 'FAILED'))
);

CREATE TABLE IF NOT EXISTS chunk (
                                      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                      document_id BIGINT NOT NULL REFERENCES document(id) ON DELETE CASCADE,
                                      chunk_index INTEGER NOT NULL,
                                      content TEXT NOT NULL,
                                      source_locator TEXT,
                                      token_count INTEGER,
                                      metadata JSONB NOT NULL DEFAULT '{}'::jsonb,
                                      create_time TIMESTAMP NOT NULL DEFAULT now(),

                                      UNIQUE (document_id, chunk_index)
);

CREATE TABLE IF NOT EXISTS chunk_embedding (
                                                chunk_id BIGINT PRIMARY KEY REFERENCES chunk(id) ON DELETE CASCADE,
                                                embedding vector(1536) NOT NULL,
                                                model TEXT NOT NULL,
                                                create_time TIMESTAMP NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_document_collection_id
    ON document(collection_id);

CREATE INDEX IF NOT EXISTS idx_chunk_document_id
    ON chunk(document_id);
